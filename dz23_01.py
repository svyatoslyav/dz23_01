#Створи цикл, який запитує у тебе числа (вводь 0 для завершення). Виводь загальну суму введених чисел.
a=0
b=1
while b!=0:
    try:
        b = int(input("Ваше число\nвведіть 0 для завершення\n"))
        if b == 0:
            break
        a=a+b
        print("Ваша сума:", a)
        print("це не число")
    except:
        print("це не число")
print("Ваше фінальне число - ", a)